package com.dc.appengine.router.nginxparser.service;

public class NgxHandleException extends Exception {

	private static final long serialVersionUID = 1L;

	public NgxHandleException() {
		super();

	}

	public NgxHandleException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public NgxHandleException(String message) {
		super(message);

	}

	public NgxHandleException(Throwable cause) {
		super(cause);
		
	}
	
	

}
